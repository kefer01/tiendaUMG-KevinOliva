void procesar(){
    String nom,ape,nit,dire;
        nom = menuPrincipal.txtNombre.getText();
        ape = menuPrincipal.txtApellido.getText();
        nit = menuPrincipal.txtNit.getText();
        dire = menuPrincipal.txtDireccion.getText();
        String sql = ""; 
        
        sql = "INSERT INTO clientes (nit_clie,nom_clie,ape_clie,dire_clie) VALUES (?,?,?,?)";
        try {
            PreparedStatement pre = conect.conexion.prepareStatement(sql);
            pre.setString(1, nom);
            pre.setString(2, ape);
            pre.setString(3, nit);
            pre.setString(4, dire);
            
            int num = pre.executeUpdate();
            if(num>0){
                JOptionPane.showMessageDialog(null, "Cliente registrado Satisfactoriamente");                
                System.exit(0);
                
            }
        } catch (SQLException ex) {
            
        }
}
