    private void propiedadesTabla(){
        String columnas[] = {"Id Producto","Producto","Precio"};
        DefaultTableModel defa = new DefaultTableModel(null,columnas);
         
        try {
            Statement orden = conect.conexion.createStatement();
            ResultSet res =conect.consulta("Select* from productos");
            
            while(res.next()){
                Object Filas[]={res.getString("cod_prod"),res.getString("nom_prod"),res.getString("precio_prod")};
                defa.addRow(Filas);
            }
            
            tablaProductos.setModel(defa);
        } catch (Exception e) {
        }
    }
 
